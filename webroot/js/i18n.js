export function getTranslation(key, replacements = {}, language = 'zh') {
    // 获取当前语言的翻译
    const translation = translations[language] || translations['zh'];

    // 获取翻译文本
    let text = translation[key] || key;

    // 替换占位符
    for (const [placeholder, value] of Object.entries(replacements)) {
        text = text.replace(`{${placeholder}}`, value);
    }

    return text;
}